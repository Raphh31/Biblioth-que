<h2>Fahrenheit 451, Ray Bradury</h2><br>


<p>Fahrenheit 451 dépeint une société futuriste,<br>
où la lecture et le savoir mettent en péril le pouvoir en place.<br>
Une unité spéciale de pompiers est chargée de brûler tous les livres<br>
afin de maintenir la population sous le joug d'un gouvernement totalitaire.</p> 



<h2>Des souris et des hommes, John Steinbeck</h2><br>


<p>L'histoire se déroule au début des années 1930 pendant la Grande Dépression.<br> 
George Milton, un homme plutôt petit, et Lennie Small,<br>
un grand colosse présentant une déficience intellectuelle,<br>
sont deux amis d'enfance.<br>
ls errent sur les routes de Californie en travaillant comme saisonniers dans des ranchs..</p> 
